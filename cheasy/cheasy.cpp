#include <bits/stdc++.h>
using namespace std;
int M, N;
int main() {
    cin >> M >> N;
    while (1) {
        int R, C, X; cin >> R >> C >> X;
        if (R == 0 && C == 0 && X == 0) {
            break;
        }
    }
    while (1) {
        int R1, C1, R2, C2; cin >> R1 >> C1 >> R2 >> C2;
        if (R1 == 0 && C1 == 0 && R2 == 0 && C2 == 0) {
            break;
        }
    }
    return 0;
}