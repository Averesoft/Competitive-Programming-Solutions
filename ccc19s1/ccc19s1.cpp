#include <bits/stdc++.h>
using namespace std;
string s; int h = 0, v = 0;
int main() {
    cin >> s;
    for (int i = 0; i < s.size(); i++) {
        if (s[i] == 'H') {
            h++;
        } else {
            v++;
        }
    }
    h %= 2; v %= 2;
    int a[2][2] = {{1, 2}, {3, 4}};
    if (h == 1) {
        a[0][0] += a[1][0];
        a[1][0] = a[0][0] - a[1][0];
        a[0][0] -= a[1][0];
        a[0][1] += a[1][1];
        a[1][1] = a[0][1] - a[1][1];
        a[0][1] -= a[1][1];
    }
    if (v == 1) {
        a[0][0] += a[0][1];
        a[0][1] = a[0][0] - a[0][1];
        a[0][0] -= a[0][1];
        a[1][0] += a[1][1];
        a[1][1] = a[1][0] - a[1][1];
        a[1][0] -= a[1][1];
    }
    cout << a[0][0] << ' ' << a[0][1] << '\n' << a[1][0] << ' ' << a[1][1];
}